package com.str;

public class ReviewStr {
public String name;
public String text;
public int stars;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getText() {
	return text;
}
public void setText(String text) {
	this.text = text;
}
public int getStars() {
	return stars;
}
public void setStars(int stars) {
	this.stars = stars;
}
}
